package com.rdynamicprogrammingadvanced;

public class WordBreak {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {

            if (i == 5)
                break;

            System.out.println("i : "+i);
        }
        System.out.println("Done");
        System.out.println();
    }
}
